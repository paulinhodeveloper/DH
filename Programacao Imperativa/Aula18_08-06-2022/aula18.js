 loopDePares = () => {
     for (let i = 1; i <= 100; i++) {
         if (i % 2 == 0) {
             console.log(' O número ' + i + ' é par ');
         }
     }
 }
 console.log(loopDePares());


loopDeImpares = () => {
     for (let i = 1; i <= 100; i++) {
         if (i % 2 == 1) {
             console.log(' O número ' + i + ' é impar ');
         }
     }
 }
 console.log(loopDeImpares());

